namespace caculate
{
    template <class T>
    void add(T &x, T y)
    {
        x += y;
        if (x >= mod)
            x -= mod;
    }
    template <class T>
    void sub(T &x, T y)
    {
        x -= y;
        if (x < 0)
            x += mod;
    }
    template <class T>
    void maximize(T &x, T y)
    {
        if (x < y)
            x = y;
    }
    template <class T>
    void minimize(T &x, T y)
    {
        if (x > y)
            x = y;
    }
}

using namespace caculate;

namespace process
{
    string s;

    void calc(){
        cin >> s;
        int res = 0;
        for (int i = 0; i < s.size(); i++)
        {
            if (s[i] == '0')
            {
                if (s[i + 1] == '1')
                    i++;
            }
            else if (s[i] == '1')
            {
                if (s[i + 1] == '0')
                    i++;
                else
                    res++;
            }
        }
        cout << res << "\n";
    }

    void process()
    {
        int n;
        cin >> n;
        while(n --){
            calc();
        }
    }
}

